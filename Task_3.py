# Написати скрипт, який видалить пусті елементи із списка. Список можна "захардкорити".
# Sample data: [(), (), ('',), ('a', 'b'), {}, ('a', 'b', 'c'), ('d'), '', []]
# Expected output: [('',), ('a', 'b'), ('a', 'b', 'c'), 'd']

list = [(), (), ('',), ('a', 'b'), {}, ('a', 'b', 'c'), ('d'), '', []]
list = [t for t in list if t]
print(list)
