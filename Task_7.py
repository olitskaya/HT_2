# Написати скрипт, який отримає максимальне і мінімальне значення із словника. Дані захардкорити.
# Приклад словника (сожете використовувати свій):
# dict_1 = {1: 10, 2: 20, 3: 30, 4: 40, 5: 50, 6: 60}
# Вихідний результат:
# MIN: 10
# MAX: 60

dict_1 = {1: 10, 2: 20, 3: 30, 4: 40, 5: 50, 6: 60}
min_key = min(dict_1, key=dict_1.get)
print('MIN: ', dict_1.get(min_key))
max_key = max(dict_1, key=dict_1.get)
print('MAX: ', dict_1.get(max_key))
